#include <iostream>
using namespace std;

int main()
{   long i,count=0,max=0;
    long arr[100000];
    cin >> i;
    for (int j=0; j<i;j++){
        cin >>arr[j];
    }
    for (int k=0; k<i;k++){
        if (arr[k]>max || arr[k]==max){
            max=arr[k];
            count++;
        }
    }
    cout <<count;

}
